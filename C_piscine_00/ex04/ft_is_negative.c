#include <unistd.h>

void	ft_is_negative(int n)
{
	char	b;
	char	c;

	if (n < 0)
	{
		b = 'N';
		write(1, &b, 1);
	}
	else
	{
		c = 'P';
		write(1, &c, 1);
	}
}
